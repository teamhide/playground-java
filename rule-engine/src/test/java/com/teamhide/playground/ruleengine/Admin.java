package com.teamhide.playground.ruleengine;

public class Admin {
    private final int age;
    private final String name;

    public Admin(final int age, final String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
